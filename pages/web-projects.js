import useTranslation from 'next-translate/useTranslation';
import Head from 'next/head';
import { useEffect, useState } from 'react';
import ProjectsLayout from "../components/layouts/projectsLayout/projectsLayout";
import Footer from '../components/shared/footer/footer';
import Nav from '../components/shared/nav/nav';
import { getDevice } from '../hooks/size';
import { fetchGraphQL } from './api/cms';

const WebProjects = () => {
    const device = getDevice();
    const { t, lang } = useTranslation('common');
    const [list, setList] = useState();

    const links = [
        {title: t("common:app_development"), image:`/home/${device}/image-app-design.jpg`, link:'/app-projects'},
        {title: t("common:articles"), image:`/home/${device}/image-graphic-design.jpg`, link:'/resources'},
    ]

    const query = `{
        projectCollection(where: { type: "web-projects"}, locale: "${lang}") {
            items {
              title
              date
              link
              technologies
              linkgithub
              auxLink
              description{
                json
              }
              image{
                url
              }
            }
          }
    }`;
    useEffect(() => {
        const fetch = async() => {
            const res = await fetchGraphQL(query); 
            console.log(Object.values(res.projectCollection.items))
            setList({
                projectCollection: Object.values(res.projectCollection.items)
            }
            
            );   
        }
        fetch();
     
    }, [lang]);
    if(!list) return;
    return(
        <div>
            <Head>
                <title>Maria Mor√°n</title>
                <meta name="description" content="Generated by create next app" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <Nav />
            <main className='page'>
                <ProjectsLayout 
                    title={t('web_development')} 
                    subtitle={t('web_development_subtitle')} 
                    list={list.projectCollection} 
                    links={links}
                />
            </main>
            <Footer />
        </div>
    )
}

export default WebProjects;